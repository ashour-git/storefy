interface DynamicIconProps extends IconProps {
  name: string;
}

export function DynamicIcon({ name, size = 20, ...props }: DynamicIconProps) {
  const cleanName = name ? name.trim() : "";
  
  // 1. Exact match in Icon Library
  if (ICONS[cleanName]) {
    const Component = ICONS[cleanName];
    return <Component size={size} {...props} />;
  }

  // 2. Check legacy emoji map
  if (EMOJI_MAP[cleanName]) {
    const resolvedName = EMOJI_MAP[cleanName];
    const Component = ICONS[resolvedName];
    return <Component size={size} {...props} />;
  }

  // 3. Fallback to sparkles if not found
  const Fallback = ICONS["sparkles"];
  return <Fallback size={size} {...props} />;
}
